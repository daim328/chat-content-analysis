def translate_body(body_text):
    """将单条消息翻译为中文"""
    if pd.isna(body_text):
        return body_text
    return TRANSLATIONS.get(str(body_text).strip(), str(body_text))


def main():
    # 路径配置
    base_dir = os.path.dirname(os.path.abspath(__file__))
    input_file  = os.path.join(base_dir, 'message20260119.xlsx')
    output_file = os.path.join(base_dir, 'message_chinese.xlsx')

    # 加载数据
    msg = pd.read_excel(input_file)
    total = len(msg)
    print(f"加载 {total} 条消息")

    # 翻译
    msg['chinese_translation'] = msg['body'].apply(translate_body)

    # 统计
    n_translated = (msg['body'] != msg['chinese_translation']).sum()
    print(f"翻译完成: {n_translated} / {total} ({n_translated / total * 100:.1f}%)")

    # 保存
    msg.to_excel(output_file, index=False)
    print(f"输出文件: {output_file}")
    print(f"列: {msg.columns.tolist()}")

    return msg


if __name__ == '__main__':
    main()
